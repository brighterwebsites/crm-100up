-- 100UP CRM — Phase 2 migration: customers, installation_requests,
-- date renames, profile notification fields.
--
-- Applied directly to the live DB via execute_sql on 2026-07-19.
-- This file is the authoritative record of those changes.
-- See docs/schema-restructure-proposal.md for full design rationale.
--
-- Scope:
--   §1  customers table  (split from jobs.name/email/phone/contact_method)
--   §2  installation_requests table  (replaces jobs.job_order jsonb)
--   §3  jobs date column renames  (planned_install_date, install_start_date,
--                                   install_completion_date)
--   §4  profiles notification columns
--   Guard trigger + advance/moveback/reschedule RPCs updated for new names.
--   RLS, grants, Realtime publications for new tables.
--
-- NOT in this migration: manufacturer normalization (§7) — see proposal doc.

-- ── §1 customers ──────────────────────────────────────────────────────
create table public.customers (
  id             bigint generated by default as identity primary key,
  name           text   not null,
  address        text   not null default '',   -- optional mailing address; distinct from jobs.location
  phone          text   not null default '',
  email          text   not null default '',
  contact_method text   not null default 'Email',
  version        integer not null default 1,
  created_at     timestamptz not null default now(),
  updated_at     timestamptz not null default now()
);

create or replace function private.bump_customer_version()
returns trigger
language plpgsql
security invoker
set search_path = ''
as $$
begin
  new.version := old.version + 1;
  new.updated_at := now();
  return new;
end;
$$;

create trigger b_bump_customer_version
  before update on public.customers
  for each row execute function private.bump_customer_version();

-- RLS
revoke all on public.customers from anon;
grant select, insert, update, delete on public.customers to authenticated;
grant usage, select on sequence public.customers_id_seq to authenticated;

alter table public.customers enable row level security;

-- Installers can SELECT a customer reachable through a job assigned to them
create policy "customers_select_admin_or_via_job" on public.customers
  for select to authenticated
  using (
    (select private.is_admin())
    or exists (
      select 1 from public.jobs j
      where j.customer_id = customers.id
        and j.assigned_installer_id = (select auth.uid())
    )
  );

create policy "customers_insert_admin" on public.customers
  for insert to authenticated
  with check ((select private.is_admin()));

create policy "customers_update_admin" on public.customers
  for update to authenticated
  using ((select private.is_admin()))
  with check ((select private.is_admin()));

create policy "customers_delete_admin" on public.customers
  for delete to authenticated
  using ((select private.is_admin()));


