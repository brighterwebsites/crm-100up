  (job_id, job_order_ref, issued_date, vehicle,
   site_access_notes, special_instructions, additional_notes,
   custom_items, created_at)
select
  id,
  coalesce(job_order->>'ref', ''),
  nullif(job_order->>'issued', '')::date,
  coalesce(job_order->>'vehicle', ''),
  coalesce(job_order->>'siteAccess', ''),
  coalesce(job_order->>'specialInstructions', ''),
  coalesce(job_order->>'extraNotes', ''),
  coalesce(job_order->'customItems', '[]'::jsonb),
  to_timestamp((job_order->>'savedAt')::bigint / 1000)
from public.jobs
where job_order is not null;


